my_string = input('Введите ваш любимый цвет: ')  # ввод с клавиатуры
print(len(my_string))  # длина строки

print(my_string.upper())  # приведение к верхнему регистру
print(my_string.lower())  # приведение к нижнему регистру
print(my_string.replace(' ', ''))  # замена пробелов на пустую строку
print(my_string[0])  # первый символ
print(my_string[-1])  # последний символ
